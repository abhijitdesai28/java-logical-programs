/**
 *Write java program which contain Decrement Operation Example
 */
public class DecrementExample {
    public static void main(String[] args) {
        int a=5;
        a++;//5(6)
        a++;//6(7)
        a--;//7(6)
        ++a;//7
        a--;//7(6)
        --a;//5
        System.out.println(--a + a++);//4+ 4(5)=8

    }
}
